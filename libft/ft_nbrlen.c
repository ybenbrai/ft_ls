#include "libft.h"

int	ft_nbrlen(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n = -n;
		i++;
	}
	if (n >= 10)
		i += ft_nbrlen(n / 10);
	else
		i++;
	return (i);
}
